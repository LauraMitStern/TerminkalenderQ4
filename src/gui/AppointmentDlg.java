    private void onDone(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_onDone
        //Wenn true wird der Eintrag sp√§ter automatisch in die DB geladen.
        boolean loadInDb = false;
        if (this.cbDB.isSelected()) {
            loadInDb = true;
        }
        this.app = new Appointment(tfName.getText(), tfLocation.getText(), date, 
                LocalTime.parse(tfTime.getText(), TIME_FORMATTER), loadInDb);
        this.isOk = true;
        this.dispose();
    }//GEN-LAST:event_onDone

